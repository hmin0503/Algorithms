T = int(input())
# 여러개의 테스트 케이스가 주어지므로, 각각을 처리합니다.
for test_case in range(1, T + 1):
    n, a = input().split()
    result = ""
    for i in range(int(n)):
        result += "{:04b}".format(int(a[i],16))
    print("#{} {}".format(test_case, result))
